import UIKit

extension UIFont {
    class func light(_ size:CGFloat) -> UIFont { return UIFont(name:"SFMono-Light", size:size)! }
    class func lightItalic(_ size:CGFloat) -> UIFont { return UIFont(name:"SFMono-LightItalic", size:size)! }
    class func regular(_ size:CGFloat) -> UIFont { return UIFont(name:"SFMono-Regular", size:size)! }
    class func regularItalic(_ size:CGFloat) -> UIFont { return UIFont(name:"SFMono-RegularItalic", size:size)! }
    class func bold(_ size:CGFloat) -> UIFont { return UIFont(name:"SFMono-Bold", size:size)! }
    class func boldItalic(_ size:CGFloat) -> UIFont { return UIFont(name:"SFMono-BoldItalic", size:size)! }
}
